function titleFromKey(key: string) {
  return key
    .replace(/([a-z0-9])([A-Z])/g, '$1 $2')
    .replace(/_/g, ' ')
    .toLowerCase()
}

function fallbackTranslation(language: Exclude<LanguageCode, 'en' | 'vi'>, key: TranslationKey): string {
  const tone = languageTone[language]
  const label = titleFromKey(key)

  if (label.includes('login')) return tone.login
  if (label.includes('create') && label.includes('account')) return tone.create
  if (label.includes('join')) return tone.join
  if (label.includes('share')) return tone.share
  if (label.includes('save')) return tone.save
  if (label.includes('cancel')) return tone.cancel
  if (label.includes('edit')) return tone.edit
  if (label.includes('delete') || label.includes('remove')) return tone.delete
  if (label.includes('close')) return tone.close
  if (label.includes('search')) return tone.search
  if (label.includes('session')) return tone.session
  if (label.includes('tournament')) return tone.tournament
  if (label.includes('club')) return tone.club

  return tone.fallbackPrefix + ': ' + label
}

function makeLanguage(language: Exclude<LanguageCode, 'en' | 'vi'>): TranslationMap {
  const translated = {} as TranslationMap
  const overrides: Partial<TranslationMap> = keyTranslations[language]

  ;(Object.keys(en) as TranslationKey[]).forEach((key) => {
    translated[key] = overrides[key] || fallbackTranslation(language, key)
  })

  return translated
}

